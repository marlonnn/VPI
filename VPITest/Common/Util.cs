        /// <summary>
        /// 大端转小端　32位
        /// </summary>
        /// <param name="readdata"></param>
        /// <returns></returns>
        public static UInt32 B2LInt32(byte[] readdata)
        {
            uint u;
            UInt32 value;
            u = (uint)(readdata[0] | readdata[1] << 8 | readdata[2] << 16 | readdata[3] << 24);
            readdata[0] = (byte)(u);
            readdata[1] = (byte)(u >> 8);
            readdata[2] = (byte)(u >> 16);
            readdata[3] = (byte)(u >> 24);
            value = BitConverter.ToUInt32(readdata, 0);
            return value;
        }

        /// <summary>
        /// 大端转小端　16位
        /// </summary>
        /// <param name="readdata"></param>
        /// <returns></returns>
        public static ushort B2LInt16(byte[] readdata)
        {
            ushort u;
            ushort value;
            u = (ushort)(readdata[0] | readdata[1] << 8);
            readdata[0] = (byte)(u);
            readdata[1] = (byte)(u >> 8);
            value = BitConverter.ToUInt16(readdata, 0);
            return value;
        }


